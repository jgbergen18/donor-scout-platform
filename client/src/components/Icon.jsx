const FILLED = new Set(['linkedin']);

export default function Icon({ name, size = 18, strokeWidth = 2, className = '' }) {
  // Ukraine flag — a crafted two-tone mark, not an emoji.
  if (name === 'flag-ua') {
    return (
      <svg width={size} height={size} viewBox="0 0 24 24" className={`icon ${className}`} aria-hidden="true">
        <rect x="3" y="6" width="18" height="6" fill="#0057B8" />
        <rect x="3" y="12" width="18" height="6" fill="#FFD700" />
      </svg>
    );
  }
  const content = PATHS[name];
  if (!content) return null;
  const filled = FILLED.has(name);
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill={filled ? 'currentColor' : 'none'}
      stroke={filled ? 'none' : 'currentColor'}
      strokeWidth={strokeWidth}
      strokeLinecap="round"
      strokeLinejoin="round"
      className={`icon ${className}`}
      aria-hidden="true"
    >
      {content}
    </svg>
  );
}

// Map a backend reason string ("👪 Possible family") to an icon name.
export function reasonIconName(text) {
  const t = (text || '').toLowerCase();
  if (t.includes('family')) return 'users';
  if (t.includes('school')) return 'cap';
  if (t.includes('coworker')) return 'building';
  if (t.includes('local')) return 'pin';
  if (t.includes('reachable')) return 'mail';
  if (t.includes('ukraine')) return 'flag-ua';
  if (t.includes('cause')) return 'book';
  if (t.includes('decision')) return 'star';
  return 'check';
}

// Strip a leading emoji/symbol from a reason string, leaving clean text.
export function stripEmoji(text) {
  return (text || '').replace(/^[^A-Za-z0-9(]+/, '').trim();
}
